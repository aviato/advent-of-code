defmodule DayTwoTest do
  use ExUnit.Case
  doctest DayTwo

  test "sum_valid_game_ids (example data)" do
    data = """
    Game 1: 3 blue, 4 red; 1 red, 2 green, 6 blue; 2 green
    Game 2: 1 blue, 2 green; 3 green, 4 blue, 1 red; 1 green, 1 blue
    Game 3: 8 green, 6 blue, 20 red; 5 blue, 4 red, 13 green; 5 green, 1 red
    Game 4: 1 green, 3 red, 6 blue; 3 green, 6 red; 3 green, 15 blue, 14 red
    Game 5: 6 red, 1 blue, 3 green; 2 blue, 1 red, 2 green
    """

    assert DayTwo.sum_valid_game_ids(data) == 8
  end

  test "sum_valid_game_ids" do
    assert DayTwo.sum_valid_game_ids() == 2541
  end

  test "sum_powers (example data)" do
    data = """
    Game 1: 3 blue, 4 red; 1 red, 2 green, 6 blue; 2 green
    Game 2: 1 blue, 2 green; 3 green, 4 blue, 1 red; 1 green, 1 blue
    Game 3: 8 green, 6 blue, 20 red; 5 blue, 4 red, 13 green; 5 green, 1 red
    Game 4: 1 green, 3 red, 6 blue; 3 green, 6 red; 3 green, 15 blue, 14 red
    Game 5: 6 red, 1 blue, 3 green; 2 blue, 1 red, 2 green
    """

    assert DayTwo.sum_powers(data) == 2286
  end

  test "sum_powers" do
    assert DayTwo.sum_powers() == 66016
  end

end
